    public void getAllHutang() {
        try {
            NumberFormat formatter = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
            List<Hutang> listNgutang = hutangStorage.getDataHutang();
        
            DefaultTableModel model = new DefaultTableModel(
                new Object[][]{},
                new String[]{"ID","Judul Hutang", "Jumlah Hutang", "Status"}
            );
        
            for (Hutang hutang : listNgutang ) {
                int id_hutang = hutang.getId_hutang();
                String judul_hutang = hutang.getJudul_hutang();
                int jumlah_hutang = hutang.getJumlah_hutang();
                String format_jumlah_hutang = formatter.format(jumlah_hutang);
                String status = hutang.getStatus();
            
                model.addRow(new Object[]{id_hutang, judul_hutang, format_jumlah_hutang, status});
            }
        
            tablehutang.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

