    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
        try {
        int selectedRow = tablehutang.getSelectedRow();
        int id_hutang = Integer.parseInt(tablehutang.getValueAt(selectedRow, 0).toString());
        String judul_hutang = tablehutang.getValueAt(selectedRow, 1).toString();
        String jumlahhutangString = tablehutang.getValueAt(selectedRow, 2).toString();
        String jumlahHutangCleaned = jumlahhutangString.replaceAll("[^\\d]", ""); 
        int jumlahHutangAwal = Integer.parseInt(jumlahHutangCleaned);
        int jumlah_hutang = jumlahHutangAwal/100;
        EditHutangGUIFORM edit = new EditHutangGUIFORM(id_hutang, judul_hutang, jumlah_hutang);
        edit.setVisible(true);
        edit.addWindowListener(new WindowAdapter() {
        public void windowClosed(WindowEvent e) {
            getAllHutang();
        }
    });
    } catch(Exception e) {
        e.printStackTrace();
}
        
    }//GEN-LAST:event_editbtnActionPerformed

    private void hapusbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tablehutang.getSelectedRow();
        int id_hutang = (int) tablehutang.getValueAt(selectedRow, 0);
        try {
        Hutang hutang = new Hutang(id_hutang);
        int rowsAffected = hutangStorage.deletePengeluaran(hutang);
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Hutang berhasil dihapus");
         } else {
            JOptionPane.showMessageDialog(null, "Gagal menghapus hutang");
        }
        }catch (SQLException e) {
            e.printStackTrace();
        }
        getAllHutang();
    }//GEN-LAST:event_hapusbtnActionPerformed
    
    private void caribtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_caribtnActionPerformed
        // TODO add your handling code here:
        String keyword = cariField.getText();
        DefaultTableModel model = (DefaultTableModel) tablehutang.getModel();
        model.setRowCount(0);
        try {
            List<Hutang> listNgutang = hutangStorage.getDataHutangByJudul(keyword);
            if(!listNgutang.isEmpty()){
                for(Hutang hutang : listNgutang){
                Object [] row = {
                hutang.getId_hutang(),
                hutang.getJudul_hutang(),
                hutang.getJumlah_hutang(),
                hutang.getStatus()
                };
                model.addRow(row);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Data tidak ada");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_caribtnActionPerformed

