    public void getAllPengeluaran() {
        try {
            NumberFormat formatter = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
            List<Pengeluaran> listPengeluaran = pengeluaranStorage.getdataPengeluaranById();
        
            DefaultTableModel model = new DefaultTableModel(
                new Object[][]{},
                new String[]{"ID","Jumlah Pengeluaran", "Nama Kategori", "Tanggal Pengeluaran", "Catatan"}
            );
        
            for (Pengeluaran pengeluaran : listPengeluaran) {
                int id_pengeluaran = pengeluaran.getId_pengeluaran();
                int jumlah_pengeluaran = pengeluaran.getJumlah_pengeluaran();
                String format_jumlah_pengeluaran = formatter.format(jumlah_pengeluaran);
                String nama_kategori = pengeluaran.getNama_kategori();
                LocalDate tanggal_pengeluaran = pengeluaran.getTanggal_pengeluaran();
                DateTimeFormatter refactorDateTimeFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                String tanggalFormatted = tanggal_pengeluaran.format(refactorDateTimeFormatter);
                String note = pengeluaran.getNote();
            
                model.addRow(new Object[]{id_pengeluaran, format_jumlah_pengeluaran, nama_kategori, tanggalFormatted, note});
            }
        
            tablePengeluaran.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        }
