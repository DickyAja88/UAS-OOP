    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
        try {
        int selectedRow = tablePengeluaran.getSelectedRow();
        int id_pengeluaran = Integer.parseInt(tablePengeluaran.getValueAt(selectedRow, 0).toString());
        String jumlahPengeluaranString = tablePengeluaran.getValueAt(selectedRow, 1).toString();
        String jumlahPengeluaranCleaned = jumlahPengeluaranString.replaceAll("[^\\d]", ""); 
        int jumlah_pengeluaran = Integer.parseInt(jumlahPengeluaranCleaned);
        String nama_kategori = tablePengeluaran.getValueAt(selectedRow, 2).toString();
        String tanggalString = tablePengeluaran.getValueAt(selectedRow, 3).toString();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate tanggal_pengeluaran = LocalDate.parse(tanggalString, formatter);
        String note = tablePengeluaran.getValueAt(selectedRow, 4).toString();

        int id_kategori_pengeluaran = pilihKategori.getKategoriId(nama_kategori); 
        EditPengeluaranGUIFORM edit = new EditPengeluaranGUIFORM(id_pengeluaran, id_kategori_pengeluaran, jumlah_pengeluaran, tanggal_pengeluaran, note);
        edit.setVisible(true);
        edit.addWindowListener(new WindowAdapter(){public void windowClosed(WindowEvent e){getAllPengeluaran();;}});
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_editbtnActionPerformed

    private void hapusbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tablePengeluaran.getSelectedRow();
        int id_pengeluaran = (int) tablePengeluaran.getValueAt(selectedRow, 0);
        try {
        Pengeluaran pengeluaran = new Pengeluaran(id_pengeluaran);
        int rowsAffected = pengeluaranStorage.deletePengeluaran(pengeluaran);
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data pengeluaran berhasil dihapus");
         } else {
            JOptionPane.showMessageDialog(null, "Gagal menghapus data pengeluaran");
        }
        }catch (SQLException e) {
            e.printStackTrace();
        }
        getAllPengeluaran();
    }//GEN-LAST:event_hapusbtnActionPerformed

    private void caribtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_caribtnActionPerformed
        // TODO add your handling code here:
        String keyword = cariField.getText();
        DefaultTableModel model = (DefaultTableModel) tablePengeluaran.getModel();
        model.setRowCount(0);
        try {
            List<Pengeluaran> listPengeluaran= pengeluaranStorage.getPengeluaranByNamaKategori(keyword);
            if (!listPengeluaran.isEmpty()){
                for(Pengeluaran pengeluaran : listPengeluaran){
                Object [] row = {
                pengeluaran.getId_pengeluaran(),
                pengeluaran.getJumlah_pengeluaran(),
                pengeluaran.getNama_kategori(),
                pengeluaran.getTanggal_pengeluaran(),
                pengeluaran.getNote(),
                };
                model.addRow(row);
                }
                 
            }else{
                JOptionPane.showMessageDialog(null, "Data tidak ada");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_caribtnActionPerformed

